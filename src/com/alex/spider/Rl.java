package com.alex.spider;

public class Rl {
	String rid; //房间号

	String rn; //房间标题
	
	String uid; //主播id

	String nn; //主播名
	
	String rs1; //头像地址
	
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getRid() {
		return rid;
	}
	public void setRid(String rid) {
		this.rid = rid;
	}
	public String getRn() {
		return rn;
	}
	public void setRn(String rn) {
		this.rn = rn;
	}
	public String getNn() {
		return nn;
	}
	public void setNn(String nn) {
		this.nn = nn;
	}
	public String getRs1() {
		return rs1;
	}
	public void setRs1(String rs1) {
		this.rs1 = rs1;
	}
}
